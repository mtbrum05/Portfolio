    private void reset() {
        txtId.setEditable(true);
        txtId.setText(null);
        txtNomeLivro.setText(null);
        txtNomeEditora.setText(null);
        txtNomeautor.setText(null);
        txtSinopse.setText(null);
        cbxgenero.setSelectedIndex(0);
        spiQuant.setValue(0);
        txtDataLanc.setText(null);        
        btnAlterarLivro.setVisible(true);
        btnDeletarLivro.setVisible(true);
        btnadicionarLivro.setVisible(true);
    }
    private void lertabela(int id){
        Comandos d1 = new Comandos(); //instanciando objeto da classe comandos, que possui as querys
        for (Livro e: d1.RetornaInfoLivro(id)){ //loop de preenchimento da tabela (fornecido o index e o complemento(where) para refinamento de buscas)
            txtNomeLivro.setText(e.getNomeLivro());
            txtNomeEditora.setText(e.getNomeEditora());
            txtNomeautor.setText(e.getNomeAutor());
            cbxgenero.setSelectedItem(e.getNomeGenero());
            System.out.println(e.getDataLancamento());
            txtDataLanc.setText((e.getDataLancamento()));
            spiQuant.setValue(e.getNumQuantidade());
            txtSinopse.setText(e.getSinopse());
            }     
        }
    private Livro preencherdados(){
        Livro livro = new Livro();
        if(txtId.isEditable() == true){
        livro.setIdLivro(Integer.parseInt(txtId.getText()));
        }
        livro.setNomeLivro(txtNomeLivro.getText());
        livro.setNomeEditora(txtNomeEditora.getText());
        livro.setNomeAutor(txtNomeautor.getText());
        livro.setNomeGenero((String) cbxgenero.getSelectedItem());
        livro.setDataLancamento(txtDataLanc.getText());
        livro.setSinopse(txtSinopse.getText());
        livro.setNumQuantidade((Integer) spiQuant.getValue());
        if (livro.getNumQuantidade() > 0) {
            livro.setStatus(1);
        } else {
            livro.setStatus(0);
        }
        return livro;
    }
    
