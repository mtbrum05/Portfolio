    private void cbxacaoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxacaoItemStateChanged

        if (cbxacao.getSelectedIndex() == 1) {
            reset();
            txtId.setEditable(false);
            btnAlterarLivro.setVisible(false);
            btnDeletarLivro.setVisible(false);
    }//GEN-LAST:event_cbxacaoItemStateChanged
        else if (cbxacao.getSelectedIndex() == 2) {
            reset();
            btnadicionarLivro.setVisible(false);
            btnDeletarLivro.setVisible(false);            
        } else if (cbxacao.getSelectedIndex() == 3) {
            reset();
            btnadicionarLivro.setVisible(false);
            btnAlterarLivro.setVisible(false);
        }

    }
