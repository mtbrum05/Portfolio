    private void brnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnExcluirActionPerformed
        // TODO add your handling code here:
        Conta c1 = new Conta();
        c1.setId(Integer.parseInt(txtId.getText()));
        c1.setSenha(String.valueOf(txtSenha.getPassword()));
        c1.setSenhab(String.valueOf(txtcSenha.getPassword()));
        if (c1.getSenha().equals(c1.getSenhab())){
            comandosDAO d1 = new comandosDAO();
            boolean check = d1.ConferirSenha(c1.getId(),c1.getSenha());
            if (check==true){
            d1.ExcluirConta(c1.getId(), c1.getSenha());
            TelaInicial t1 = new TelaInicial();
            t1.setVisible(true);
            dispose();     
            }       
        }else{
            JOptionPane.showMessageDialog(null, "AS SENHAS N√ÉO COINCIDEM!");
        }
        
        
        
        
    }//GEN-LAST:event_brnExcluirActionPerformed

