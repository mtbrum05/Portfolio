        private String lertabela(String p){
        DefaultTableModel modelo = (DefaultTableModel) jTabelaTitulo.getModel(); //definindo modelo da tabela
        jTabelaTitulo.setRowSorter(new TableRowSorter(modelo));
        Comandos d1 = new Comandos(); //instanciando objeto da classe comandos, que possui as querys
        modelo.setNumRows(0); //setando n√∫mero inicial de linhas da tabela
        String sin = null;
        for (Livro e: d1.ConsultaLivro2(p)){ //loop de preenchimento da tabela (fornecido o index e o complemento(where) para refinamento de buscas)
            sin =  e.getSinopse(); //guardando a sinopse em uma variavel
            modelo.addRow(new Object[]{ //preenchendo linhas da tabela com atributos de um objeto da classe Emprestimo
            e.getNomeLivro(),
            e.getNomeGenero(),
            e.getStatusFinal()
            });
            }
          return sin;
        }

